#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i > 0 && s[i] != (char)c)
		i--;
	if (i || (!i && s[i] == c))
		return ((char*)(s + i));
	else
		return (0);
}
